package br.com.fvm.service;

public class Resposta {
	private String title;
	private int x,y;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String cordenada) {
		this.title = cordenada;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	

}
